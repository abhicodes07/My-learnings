# Printing all the datatypes in python
a = 'Hello world'
print(type(a))
b = 20
print(type(b))
c = 30.1
print(type(c))
d = 3j
print(type(d))
e = ["My", "name", "is", "Abhi"]
print(e)
print(type(e))
f = ("This", "is", "Abhinav")
print(f)
print(type(f))
g = range(10)
print(g)
print(type(g))
h = {"Name":"Abhinav","Age":19}
print(h)
print(type(h))
i = {"My", "is", "abhinav", "asthana."}
print(i)
print(type(i))
j = frozenset({"I", "am", "Abhinav asthana."})
print(j)
print(type(j))
k = True
print(k)
print(type(k))
l = b"Abhi"
print(l)
print(type(l))
m = memoryview(bytes(6))
print(m)
print(type(m))
o = None
print(o)
print(type(o))

