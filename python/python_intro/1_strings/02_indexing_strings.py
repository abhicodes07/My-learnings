# ACCESSING STRING CHARACTERS USING STRING INDEXING 
string1 = input("Enter the string: ")
print("Entered string is: ", string1)

# Accessing the first character of the string 
frst_ch = string1[0]
print("The first character of the string is: ", frst_ch)

# Accessing the last character of the string
lst_ch = string1[-1]
print("The last character of the string: ", lst_ch)
