# String Slicing to access range of characters from strings
str1 = "iamabhinavasthana"
print("Entered string: ", str1)

# Printing characters from 3 to 12th of a string
print("Slicing of a string from 3rd to 12th: ")
print(str1[3:12])

# Printing characters from 3 to -1 of a string
print("Slicing of a string from 3 to -1: ")
print(str1[3:-1])

